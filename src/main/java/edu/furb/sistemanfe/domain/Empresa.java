package edu.furb.sistemanfe.domain;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;
import javax.persistence.TableGenerator;

@Entity
@Table(name="TBEMPRESA")
@TableGenerator(name="GenEmpresa", table="TBSEQUENCIAS", pkColumnName="CDSEQUENCIA", pkColumnValue="EMPRESASEQ", valueColumnName="VLSEQUENCIA")
public class Empresa implements Serializable{

	private static final long serialVersionUID = 1L;
	
	@Id
	@Column(name="IDEMPRESA")
	@GeneratedValue(generator="GenEmpresa", strategy=GenerationType.TABLE)
	private Long id;
	@Column(name="NMEMPRESA", length=120)
    private String nome;
	@Column(name="FLGSTATUS", length=1)
	private String status;
	
	public Empresa() {
		
	}
	
	public Empresa(String nome, String status) {
		this.nome = nome;
		this.status = status;
	}
	
	public Long getId() {
		return id;
	}
	
	public void setId(Long id) {
		this.id = id;
	}	
	
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		result = prime * result + ((nome == null) ? 0 : nome.hashCode());
		result = prime * result + ((status == null) ? 0 : status.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Empresa other = (Empresa) obj;
		if (nome == null) {
			if (other.nome != null)
				return false;
		} else if (!nome.equals(other.nome))
			return false;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		if (status == null) {
			if (other.status != null)
				return false;
		} else if (!status.equals(other.status))
			return false;
		return true;
	}
	
}
